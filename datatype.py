
#Here's an example of data type in Python:

# 1. Integer

numbers = 5
print(numbers) # Output: (5, 10)
print(type(numbers)) # Output: <class 'int'>

# 2. Float

numbers = 5.10
print(numbers) # Output: (5.10)
print(type(numbers)) # Output: <class 'float'>

# 3. String

name = "Purna"
print(name) # Output: (Purna)
print(type(name)) # Output: <class 'str'>

# 4. List

number = [1, 2, 3, 4, 5]
print(number) # Output: [1, 2, 3, 4, 5]
print(type(number)) # Output: <class 'list'>

# 5. Tuple

number = (1, 2, 3, 4, 5)
print(number) # Output: (1, 2, 3, 4, 5)
print(type(number)) # Output: <class 'tuple'>

# 6. Dictionary

number = {"name": "Purna", "age": 22, "phone": "9829845767"}
print(number) # Output: {'name': 'Purna', 'age': 22, 'phone': '9829845767'}
print(type(number)) # Output: <class 'dict'>

# 7. Boolean

number = True
print(number) # Output: True
print(type(number)) # Output: <class 'bool'>

# 8. Set

number = {1, 2, 3, 4, 5}
print(number) # Output: {1, 2, 3, 4, 5}
print(type(number)) # Output: <class 'set'>

# 9. Complex

number = 1 + 2j
print(number) # Output: (1+2j)
print(type(number)) # Output: <class 'complex'>

# 10. Range

number = range(5)
print(number) # Output: range(0, 5)
print(type(number)) # Output: <class 'range'>

# 11. Bytes

number = b"Hello"
print(number) # Output: b'Hello'
print(type(number)) # Output: <class 'bytes'>
